function check() {
    let regionCode = document.getElementById('inputPlate').value.toLowerCase();

    Object.values(REGION_TO_SVG_ID).forEach(svgId => {
        const pathElement = document.getElementById(svgId);
        if (pathElement) {
            pathElement.style.fill = '';
        }
    });

    if (UA_REGION_CODES.hasOwnProperty(regionCode)) {
        const regionName = UA_REGION_CODES[regionCode];
        const svgId = REGION_TO_SVG_ID[regionName];

        document.getElementById('result').innerHTML = regionName;
        document.getElementById('result').style.color = "var(--green)";

        if (svgId) {
            const pathElement = document.getElementById(svgId);
            if (pathElement) {
                pathElement.style.fill = 'var(--green)';
            }
        }
    } else {
        document.getElementById('result').innerHTML = "Місто / область";
        document.getElementById('result').style.color = "var(--dark_green)";
    }
}

